package org.jcontainer.loom.interfaces;

import java.io.File;
import java.util.Map;
import org.jcontainer.loom.components.util.profile.PartitionProfile;
import org.codehaus.spice.loggerstore.LoggerStore;

/**
 * @author Peter Donald
 */
public interface Kernel
{
    /**
     * Adds an application to the container
     */
    void addApplication( PartitionProfile profile,
                         File homeDirectory, File workDirectory,
                         ClassLoader classLoader,
                         LoggerStore store,
                         Map classloaders )
        throws Exception;

    /**
     * Removes the application from the container
     *
     * @param name the name of application to remove
     */
    void removeApplication( String name )
        throws Exception;

    /**
     * Gets the named application
     *
     * @param name the name of application
     */
    Application getApplication( String name );

    /**
     * Gets the list of applications running in the container
     *
     * @return applicationNames The array of application names
     */
    String[] getApplicationNames();

    /**
     * Lock the kernel, temporarily preserving the list of applications running
     * in the container
     */
    void lock();

    /**
     * Unlock the kernel, restoring the list of applications to be the current
     * active list
     */
    void unlock();
}
